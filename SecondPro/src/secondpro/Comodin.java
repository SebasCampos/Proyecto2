    public int corro(){
        nume1 = (int) num1.getValue();
        nume2 = (int) num2.getValue();
        nume3 = (int) num3.getValue();
        int cont = 0;
        if (nume1 == code.get(0)) {
            cont ++;
        }
        if (nume2 == code.get(1)) {
            cont++;
        }
        if (nume3 == code.get(2)) {
            cont ++;
        }
        
        return cont;
    
    }
